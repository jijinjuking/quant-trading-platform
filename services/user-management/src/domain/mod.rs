//! # 领域层 (Domain Layer)
//!
//! 本模块是用户管理服务的核心领域层，包含所有业务逻辑和规则。
//!
//! ## 所属层
//! Domain Layer - DDD 架构的核心层
//!
//! ## 职责
//! - 定义领域模型（实体、值对象、聚合）
//! - 定义领域服务（跨实体的业务规则）
//! - 定义端口（抽象接口，供基础设施层实现）
//!
//! ## 依赖规则
//! - ❌ 不依赖任何外部框架（如 axum、sqlx）
//! - ❌ 不依赖 infrastructure 层
//! - ✅ 只依赖标准库和 shared 内核

/// 领域模型 - 实体、值对象、聚合
pub mod model;

/// 领域服务 - 跨实体的业务规则
pub mod service;

/// 端口定义 - 抽象接口（trait）
pub mod port;
