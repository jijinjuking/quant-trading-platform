//! # 领域层 (Domain Layer)
//!
//! 领域层是 DDD 架构的核心，包含所有业务逻辑和领域模型。
//!
//! ## 模块结构
//! - [`model`]: 领域模型（实体、值对象、聚合）
//! - [`logic`]: 领域逻辑（业务规则、算法）
//! - [`service`]: 领域服务（跨模型的业务规则）
//! - [`port`]: 端口定义（trait，六边形架构的核心）
//!
//! ## 规则
//! - 领域层不依赖任何外部层
//! - 只使用标准库和共享内核中的类型
//! - 端口（port）只定义 trait，由基础设施层实现

/// 领域模型 - 实体、值对象、聚合
pub mod model;

/// 领域逻辑 - 业务规则和算法
pub mod logic;

/// 领域服务 - 跨模型的业务规则
pub mod service;

/// 端口定义 - trait 接口（六边形架构）
pub mod port;
