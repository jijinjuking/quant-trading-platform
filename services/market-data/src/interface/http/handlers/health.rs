//! # 健康检查处理器 (Health Check Handler)
//! 
//! 提供服务健康状态检查接口。
//! 
//! ## 用途
//! - Kubernetes 存活探针 (Liveness Probe)
//! - 负载均衡器健康检查
//! - 服务监控

// ============================================================================
// 外部依赖导入
// ============================================================================

use axum::Json;          // JSON 响应包装
use serde::Serialize;    // 序列化 trait

// ============================================================================
// 响应 DTO
// ============================================================================

/// 健康检查响应 - 返回服务状态信息
#[derive(Serialize)]
pub struct HealthResponse {
    /// 服务状态 - "healthy" 或 "unhealthy"
    pub status: String,
    /// 服务名称 - 用于识别服务
    pub service: String,
}

// ============================================================================
// 处理器函数
// ============================================================================

/// 健康检查处理器
/// 
/// 返回服务的健康状态。
/// 
/// # 返回
/// - JSON 格式的健康状态信息
/// 
/// # 示例响应
/// ```json
/// {
///   "status": "healthy",
///   "service": "market-data"
/// }
/// ```
pub async fn health_check() -> Json<HealthResponse> {
    Json(HealthResponse {
        status: "healthy".to_string(),
        service: "market-data".to_string(),
    })
}
