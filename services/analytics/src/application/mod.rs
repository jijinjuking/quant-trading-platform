//! # Application Layer - 应用层
//!
//! ## 层级职责
//! 应用层负责用例编排，是业务流程的协调者：
//! - 接收接口层的请求
//! - 协调领域对象完成业务流程
//! - 调用基础设施层（通过端口trait）
//!
//! ## 架构约束
//! - ✅ 只依赖 `domain::port` 中的 trait
//! - ✅ 只负责流程编排
//! - ❌ 禁止包含业务规则（属于领域层）
//! - ❌ 禁止直接使用 SQL/HTTP/Redis 等
//!
//! ## 子模块
//! - `service`: 应用服务（用例实现）

